#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	j;
	int				i;

	j = 0;
	i = 0;
	if (s && f)
		while (s[i])
			(*f)(j++, &s[i++]);
}
